#!/usr/bin/env python3
"""Export ai-api-tester report.json as Agentic QA Gate automation evidence."""

import argparse
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))

from ai_api_tester.automation_evidence import build_automation_evidence, load_json, write_json


def parse_args():
    parser = argparse.ArgumentParser(
        description="Export ai-api-tester report.json to Agentic QA Gate automation_results.json."
    )
    parser.add_argument("report", help="JSON report generated by run_tests.py")
    parser.add_argument("--analysis", help="Optional analysis.json generated by analyze_failures.py")
    parser.add_argument("--output", "-o", required=True, help="automation_results.json output path")
    parser.add_argument("--run-id", help="Override run_id")
    parser.add_argument("--suite-name", help="Override suite name")
    parser.add_argument("--triggered-by", default="agentic_qa_gate")
    parser.add_argument("--source-tool", default="ai-api-tester")
    parser.add_argument(
        "--risk-domain",
        action="append",
        default=[],
        help="Extra risk domain covered by this run. Can be passed multiple times.",
    )
    parser.add_argument("--note", action="append", default=[], help="Extra note in the output.")
    return parser.parse_args()


def main():
    args = parse_args()
    report_path = Path(args.report)
    if not report_path.exists():
        print(f"Report not found: {report_path}", file=sys.stderr)
        sys.exit(1)

    report = load_json(report_path)
    analysis = None
    if args.analysis:
        analysis_path = Path(args.analysis)
        if not analysis_path.exists():
            print(f"Analysis not found: {analysis_path}", file=sys.stderr)
            sys.exit(1)
        analysis = load_json(analysis_path)

    payload = build_automation_evidence(
        report,
        analysis,
        run_id=args.run_id,
        triggered_by=args.triggered_by,
        source_tool=args.source_tool,
        suite_name=args.suite_name,
        risk_domains=args.risk_domain,
        report_path=report_path,
        notes=args.note,
    )
    write_json(args.output, payload)
    print(f"Automation evidence saved: {args.output}")


if __name__ == "__main__":
    main()
